const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  address: {
    type: String,
  },
  description: {
    type: String,
  },
});

const UserModel = mongoose.model('User', userSchema);

module.exports = UserModel;
